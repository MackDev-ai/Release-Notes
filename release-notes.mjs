/**
 * Release Notes Generator
 * Użycie: node release-notes.mjs <tag>
 * Przykład: node release-notes.mjs uat-version-20260316185950
 *
 * Wymagane zmienne środowiskowe w pliku .env:
 *   GITHUB_TOKEN      – Personal Access Token (repo scope)
 *   JIRA_EMAIL        – email do Jira Cloud
 *   JIRA_API_TOKEN    – token Jira API
 *   JIRA_BASE_URL     – np. https://restaumatic.atlassian.net
 *   GOOGLE_DOC_ID     – ID dokumentu Google Docs (z URL)
 *   GOOGLE_SA_KEY     – ścieżka do pliku JSON z kluczem Service Account
 */

